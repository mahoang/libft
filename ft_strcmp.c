#include "libft.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	int c;

	c = 0;
	while (s1[c] == s2[c] & s1[c] != '\0' && s2[c] != '\0')
		c++;
	return (s1[c] - s2[c]);
}
